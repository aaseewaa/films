async def seed_article(db: AsyncSession, conf: dict) -> int | None:
    """Создаёт статью. Возвращает id."""
    log.info("📝 Статья: %s", conf["title"])

    # Проверка существования по slug
    existing_sql = text("""
        SELECT et.entity_id FROM entity_translation et
        JOIN entity e ON e.id = et.entity_id
        WHERE e.entity_type = 'article'
          AND et.slug = :slug
          AND et.language_id = (SELECT id FROM language WHERE code = 'ru')
        LIMIT 1
    """)
    existing_id = (await db.execute(existing_sql, {"slug": conf["slug"]})).scalar_one_or_none()
    if existing_id:
        log.info("    уже существует (id=%d), пропускаю", existing_id)
        return existing_id

    # 1) Берём обложку у режиссёра (его primary_image_url) если есть
    director_key = conf.get("main_director_key")
    director_id = DIRECTOR_IDS.get(director_key) if director_key else None
    cover_url = None
    if director_id:
        sql = text("SELECT primary_image_url FROM entity WHERE id = :id")
        cover_url = (await db.execute(sql, {"id": director_id})).scalar_one_or_none()

    # 2) Создаём entity
    article_entity_id = await get_or_create_entity(
        db, entity_type="article", primary_image_url=cover_url,
    )

    # 3) Запись в article
    await db.execute(text("""
        INSERT INTO article (id, article_type, author_user_id, reading_time_min, is_featured, extra_metadata)
        VALUES (:id, :atype, :auid, :rtime, :feat, '{}'::jsonb)
    """), {
        "id": article_entity_id,
        "atype": conf["article_type"],
        "auid": EDITORIAL_USER_ID,
        "rtime": conf.get("reading_time"),
        "feat": conf.get("is_featured", False),
    })

    # 4) Устанавливаем published_at
    await db.execute(text("""
        UPDATE entity SET published_at = now() WHERE id = :id
    """), {"id": article_entity_id})

    # 5) Перевод (русский)
    await db.execute(text("""
        INSERT INTO entity_translation (entity_id, language_id, search_config, slug, title, summary, description, body)
        VALUES (
            :id,
            (SELECT id FROM language WHERE code = 'ru'),
            'russian',
            :slug, :title, :summary, :summary, :body
        )
    """), {
        "id": article_entity_id,
        "slug": conf["slug"],
        "title": conf["title"],
        "summary": conf["summary"],
        "body": conf["body"],
    })

    # 6) Связь с главным режиссёром
    if director_id:
        await db.execute(text("""
            INSERT INTO article_entity_link
                (article_id, entity_id, link_type, link_weight, confidence, note)
            VALUES (:aid, :eid, 'about', 1.0, 1.0, 'Главный субъект статьи')
            ON CONFLICT (article_id, entity_id, link_type) DO NOTHING
        """), {"aid": article_entity_id, "eid": director_id})

    await db.commit()
    log.info("    создана: id=%d", article_entity_id)
    return article_entity_id


