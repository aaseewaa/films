def normalize_slug(s: str) -> str:
    """Безопасный slug для URL."""
    s = s.lower().strip()
    s = re.sub(r"[^a-zа-я0-9\s-]", "", s)
    s = re.sub(r"\s+", "-", s)
    s = re.sub(r"-+", "-", s)
    return s.strip("-")[:255]


async def get_or_create_entity(
    db: AsyncSession,
    *,
    entity_type: str,
    primary_image_url: str | None = None,
) -> int:
    """Создаёт пустую сущность entity и возвращает её id."""
    sql = text("""
        INSERT INTO entity (entity_type, status, primary_image_url, external_ids, extra_metadata)
        VALUES (CAST(:etype AS entity_type), 'published', :img, '{}'::jsonb, '{}'::jsonb)
        RETURNING id
    """)
    return (await db.execute(sql, {
        "etype": entity_type, "img": primary_image_url,
    })).scalar_one()


async def ensure_taxonomy_term(
    db: AsyncSession,
    *,
    term_type: str,
    code: str,
) -> int:
    """Создаёт taxonomy_term если ещё нет. Возвращает id."""
    sql = text("""
        INSERT INTO taxonomy_term (term_type, code, is_system, sort_order)
        VALUES (CAST(:tt AS taxonomy_type), :code, true, 0)
        ON CONFLICT (term_type, code) DO UPDATE SET code = EXCLUDED.code
        RETURNING id
    """)
    return (await db.execute(sql, {"tt": term_type, "code": code})).scalar_one()


async def find_film_id_by_title(
    db: AsyncSession,
    title_ru: str,
) -> int | None:
    """Ищет фильм по русскому title. Возвращает entity_id или None."""
    sql = text("""
        SELECT et.entity_id
        FROM entity_translation et
        JOIN entity e ON e.id = et.entity_id
        WHERE e.entity_type = 'film'
          AND et.language_id = (SELECT id FROM language WHERE code = 'ru')
          AND lower(et.title) = lower(:title)
        LIMIT 1
    """)
    return (await db.execute(sql, {"title": title_ru})).scalar_one_or_none()


