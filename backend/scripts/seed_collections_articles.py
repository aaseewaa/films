async def seed_collection(db: AsyncSession, conf: dict) -> int | None:
    """Создаёт одну коллекцию с фильмами. Возвращает id коллекции."""
    log.info("📚 Коллекция: %s", conf["title_ru"])

    # Проверяем не создана ли уже (по slug в entity_translation для ru)
    existing_sql = text("""
        SELECT et.entity_id FROM entity_translation et
        JOIN entity e ON e.id = et.entity_id
        WHERE e.entity_type = 'collection'
          AND et.slug = :slug
          AND et.language_id = (SELECT id FROM language WHERE code = 'ru')
        LIMIT 1
    """)
    existing_id = (await db.execute(existing_sql, {"slug": conf["slug"]})).scalar_one_or_none()
    if existing_id:
        log.info("    уже существует (id=%d), пропускаю", existing_id)
        return existing_id

    # 1) Ищем фильмы заранее, чтобы взять обложку
    found_films: list[tuple[int, str]] = []
    for title in conf["film_titles_ru"]:
        fid = await find_film_id_by_title(db, title)
        if fid:
            found_films.append((fid, title))
            log.info("    ✓ найден: %s (id=%d)", title, fid)
        else:
            log.info("    ✗ не найден в БД: %s", title)

    # 2) Создаём entity для коллекции
    cover_url = None
    cover_entity_id = None
    if found_films:
        cover_entity_id = found_films[0][0]
        # Берём primary_image_url от cover-фильма
        cov_sql = text("SELECT primary_image_url FROM entity WHERE id = :id")
        cover_url = (await db.execute(cov_sql, {"id": cover_entity_id})).scalar_one_or_none()

    collection_entity_id = await get_or_create_entity(
        db, entity_type="collection", primary_image_url=cover_url,
    )

    # 3) Запись в collection (наследник entity)
    # 3) Запись в collection (наследник entity)
    await db.execute(text("""
        INSERT INTO collection (id, owner_user_id, kind, is_system, cover_entity_id, items_count, extra_metadata)
        VALUES (:id, NULL, 'editorial', true, :cov, :cnt, CAST(:meta AS jsonb))
    """), {
        "id": collection_entity_id,
        "cov": cover_entity_id,
        "cnt": len(found_films),
        "meta": '{"is_featured": ' + ("true" if conf.get("is_featured") else "false") + '}',
    })

    # 4) Перевод (русский)
    await db.execute(text("""
        INSERT INTO entity_translation (entity_id, language_id, search_config, slug, title, summary, description)
        VALUES (
            :id,
            (SELECT id FROM language WHERE code = 'ru'),
            'russian',
            :slug, :title, :summary, :description
        )
    """), {
        "id": collection_entity_id,
        "slug": conf["slug"],
        "title": conf["title_ru"],
        "summary": conf["summary_ru"],
        "description": conf["description_ru"],
    })

    # 5) Теги через entity_taxonomy
    for tag in conf.get("tags", []):
        term_id = await ensure_taxonomy_term(db, term_type="tag", code=tag)
        await db.execute(text("""
            INSERT INTO entity_taxonomy (entity_id, term_id, is_primary)
            VALUES (:eid, :tid, false)
            ON CONFLICT (entity_id, term_id) DO NOTHING
        """), {"eid": collection_entity_id, "tid": term_id})

    # 6) collection_item
    for position, (film_id, _title) in enumerate(found_films, start=1):
        await db.execute(text("""
            INSERT INTO collection_item (collection_id, entity_id, position, added_by_user_id)
            VALUES (:cid, :eid, :pos, :user)
            ON CONFLICT (collection_id, entity_id) DO NOTHING
        """), {
            "cid": collection_entity_id,
            "eid": film_id,
            "pos": position,
            "user": EDITORIAL_USER_ID,
        })

    await db.commit()
    log.info("    создана: id=%d, фильмов=%d", collection_entity_id, len(found_films))
    return collection_entity_id


